for (var i = 1; i <= 5; i++) {
  console.log(i);
}
console.log("Counting completed!");

for (var i = 5; i >= 1; i--) {
  console.log(i);
}
console.log("Countdown finished!");

var c = 1;
while (c <= 5) {
  console.log(c);
  c++;
}
console.log("Counting completed!");

var i = 5;
while (i > 0) {
  console.log(i);
  i--;
}
console.log("Countdown finished!");

var year = 2018;
while (year < 2023) {
  console.log(year);
  year++;
}
